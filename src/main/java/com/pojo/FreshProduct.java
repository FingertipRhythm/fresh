package com.pojo;

import java.io.Serializable;

/*
 * 商品实体类
 */
public class FreshProduct implements Serializable {
	private Integer productid; 
	private String productname;
	private String originplace; 
	private String specifications; 
	private String grade; 
	private Integer inventory; 
	private String storehouse; 
	private Double price; 
	private String sf; 
	private Integer classid; //分类id
	private Giveuser giveruser;
	public Integer getProductid() {
		return productid;
	}
	public void setProductid(Integer productid) {
		this.productid = productid;
	}
	 
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getOriginplace() {
		return originplace;
	}
	public void setOriginplace(String originplace) {
		this.originplace = originplace;
	}
	public String getSpecifications() {
		return specifications;
	}
	public void setSpecifications(String specifications) {
		this.specifications = specifications;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public Integer getInventory() {
		return inventory;
	}
	public void setInventory(Integer inventory) {
		this.inventory = inventory;
	}
	public String getStorehouse() {
		return storehouse;
	}
	public void setStorehouse(String storehouse) {
		this.storehouse = storehouse;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getSf() {
		return sf;
	}
	public void setSf(String sf) {
		this.sf = sf;
	}
	public Integer getClassid() {
		return classid;
	}
	public void setClassid(Integer classid) {
		this.classid = classid;
	}
	public Giveuser getGiveruser() {
		return giveruser;
	}
	public void setGiveruser(Giveuser giveruser) {
		this.giveruser = giveruser;
	}
	public FreshProduct() {
		super();
		// TODO Auto-generated constructor stub
	}
	public FreshProduct(Integer productid, String productname, String originplace, String specifications, String grade,
			Integer inventory, String storehouse, Double price, String sf, Integer classid, Giveuser giveruser) {
		super();
		this.productid = productid;
		this.productname = productname;
		this.originplace = originplace;
		this.specifications = specifications;
		this.grade = grade;
		this.inventory = inventory;
		this.storehouse = storehouse;
		this.price = price;
		this.sf = sf;
		this.classid = classid;
		this.giveruser = giveruser;
	}
	
}
